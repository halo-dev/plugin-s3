export interface AttachmentSpec {
    /** Display name of attachment */
    displayName?: any;
    /** Group name */
    groupName?: any;
    /** Media type of attachment */
    mediaType?: any;
    /** Name of User who uploads the attachment */
    ownerName?: any;
    /** Policy name */
    policyName?: any;
    /** Size of attachment. Unit is Byte */
    size?: any;
    /** Tags of attachment */
    tags?: any;
}
